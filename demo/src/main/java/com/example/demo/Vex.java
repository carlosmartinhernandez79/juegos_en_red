package com.example.demo;

public class Vex {
	
	private float x;
	private float y;
	
	public Vex(float newX, float newY) {
		x = newX;
		y = newY;
	}
	
	public float getX() {
		return x;
	}
	public float getY() {
		return y;
	}
	
	public void setX(float newX) {
		x = newX;
	}
	
	public void setY(float newY) {
		y = newY;
	}
}
