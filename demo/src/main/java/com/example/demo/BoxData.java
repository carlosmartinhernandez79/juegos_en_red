package com.example.demo;

public class BoxData {
    private int x;
    private int y;

    public BoxData(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Métodos getters y setters según sea necesario
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
